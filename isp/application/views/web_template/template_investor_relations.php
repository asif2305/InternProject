	<h3 style="background:#ffd505;">Business Details </h3>
	<br/><br/>
	<p>
              Vertex3 Soft operates a wireless 4G mobile data network based on IEEE 802.16e standard of ITU which is popularly known as WiMAX technology under a BWA license awarded by Bangladesh Telecommunication Regulatory Commission (BTRC).
<br/><br/>
Vertex3 Soft launched it service initially in key areas of Dhaka in January 2010 to provide high-speed wireless broadband internet service to the home and business users of those areas. At present Vertex3 Soft network is spread across 7 divisional headquarters and more than 30 major districts of the country. Vertex3 Soft management projects that by the end of 2012 the network will expand to Upozila level to cover the rural population of the country. At present Vertex3 Soft network includes more than 600 base stations.
<br/><br/>
In addition to basic high-speed internet connectivity, Vertex3 Soft is planning to gradually launch new, innovative and useful Value Added Services like IP Telephony, IP PBX, IP Surveillance, E-Health, E-Entertainment etc.

